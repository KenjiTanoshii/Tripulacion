let current = 0;

const page =
document.getElementById("page");

const pageContent =
document.getElementById("pageContent");

const pageFlip =
document.getElementById("pageFlip");

const cover =
document.getElementById("cover");

const book =
document.getElementById("book");

const nextBtn =
document.getElementById("nextBtn");

const prevBtn =
document.getElementById("prevBtn");

/* ==========================
   RENDER
========================== */

function renderPage(){

page.style.backgroundImage =
`url("${pages[current].image}")`;

pageContent.innerHTML =
pages[current].content;

}

/* ==========================
   ABRIR LIBRO
========================== */

cover.addEventListener("click",()=>{

if(cover.classList.contains("open")) return;

cover.classList.add("open");

setTimeout(()=>{

book.classList.add("visible");

},700);

});

/* ==========================
   VOLVER A PORTADA
========================== */

function goToCover(){

book.classList.remove("visible");

cover.classList.remove("open");

/* ocultamos contenido */

pageContent.style.opacity = "0";

setTimeout(()=>{

current = 0;

renderPage();

pageContent.style.opacity = "1";

},1600);

}

/* ==========================
   ANIMACIÓN DE PÁGINA
========================== */

function animatePageTurn(nextIndex){

if(nextIndex < 0) return;

if(nextIndex >= pages.length) return;

pageFlip.classList.add("flipping");

setTimeout(()=>{

current = nextIndex;

renderPage();

},700);

setTimeout(()=>{

pageFlip.classList.remove("flipping");

},1400);

}

/* ==========================
   CIERRE CON ANIMACIÓN
========================== */

function animateCloseBook(){

pageFlip.classList.add("flipping");

setTimeout(()=>{

goToCover();

},700);

setTimeout(()=>{

pageFlip.classList.remove("flipping");

},1400);

}

/* ==========================
   SIGUIENTE
========================== */

nextBtn.addEventListener("click",()=>{

if(current < pages.length - 1){

animatePageTurn(current + 1);

}
else{

animateCloseBook();

}

});

/* ==========================
   ANTERIOR
========================== */

prevBtn.addEventListener("click",()=>{

if(current > 0){

animatePageTurn(current - 1);

}
else{

goToCover();

}

});

/* ==========================
   TECLADO
========================== */

document.addEventListener("keydown",(e)=>{

if(e.key === "ArrowLeft"){

if(current > 0){

animatePageTurn(current - 1);

}
else{

goToCover();

}

}

if(e.key === "ArrowLeft"){

if(current > 0){

animatePageTurn(current - 1);

}
else{

animateCloseBook();

}

}

});

/* ==========================
   INICIO
========================== */

renderPage();
